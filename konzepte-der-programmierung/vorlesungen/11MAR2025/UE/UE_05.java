public class UE_05 {
    public static void main(String[] args) {
        int zahl = 10;
        System.out.println(zahl + 5);
        System.out.println(zahl + 3.0);
        System.out.println(zahl + 2147483647);
        System.out.println(zahl * 10);
        System.out.println(zahl / 2);
        System.out.println(zahl / 3);
        System.out.println(zahl / 3.0);
        System.out.println(zahl + "Test");
        System.out.println("Test" + zahl);
        System.out.println();
        System.out.println("17 / 5 = " + 17 / 5);
        System.out.println("Rest von 17 / 5 = " + 17 % 5);
    }
}